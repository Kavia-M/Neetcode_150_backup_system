const puppeteer = require('puppeteer');
const { marked }  = require('marked');
const fs   = require('fs');
const path = require('path');
const https = require('https');
const http  = require('http');

const BACKUP       = path.join(__dirname, 'NeetCode_150_Backup');
const META_FILE    = path.join(BACKUP, 'metadata.json');
const TOPICS_FILE  = path.join(__dirname, 'topicsAndProblems.md');
const OUT_PDF      = path.join(__dirname, 'NeetCode_150_Book.pdf');

// ── Image download + base64 embed ────────────────────────────────────────────
function fetchImage(url) {
  return new Promise((resolve) => {
    const lib = url.startsWith('https') ? https : http;
    lib.get(url, (res) => {
      const chunks = [];
      res.on('data', c => chunks.push(c));
      res.on('end', () => {
        const buf = Buffer.concat(chunks);
        const ct  = res.headers['content-type'] || 'image/png';
        resolve(`data:${ct};base64,${buf.toString('base64')}`);
      });
    }).on('error', () => resolve(url));
  });
}

async function embedImages(mdText) {
  const imgRe = /!\[([^\]]*)\]\((https?:\/\/[^)]+)\)/g;
  const matches = [...mdText.matchAll(imgRe)];
  for (const m of matches) {
    const dataUri = await fetchImage(m[2]);
    mdText = mdText.replace(m[0], `![${m[1]}](${dataUri})`);
  }
  return mdText;
}

// ── Markdown helpers ─────────────────────────────────────────────────────────
function normalizeCodeFences(md) {
  return md.replace(/^```\w+/gm, '```');
}

function expandListIndents(md) {
  return md.split('\n').map(line => {
    const m = line.match(/^( +)([-*+]|\d+\.)\s/);
    if (m) {
      const newSpaces = Math.ceil(m[1].length / 2) * 4;
      return ' '.repeat(newSpaces) + line.trimStart();
    }
    return line;
  }).join('\n');
}

function mdToHtml(md) {
  md = normalizeCodeFences(md);
  md = expandListIndents(md);
  return marked.parse(md);
}

// ── Parse topicsAndProblems.md ───────────────────────────────────────────────
function parseSections(meta) {
  const nameToNum = {};
  for (const p of meta.problems) {
    nameToNum[p.name.toLowerCase().trim()] = p.num;
  }

  const sections = [];
  let curSection = null, curProblems = [];

  for (const rawLine of fs.readFileSync(TOPICS_FILE, 'utf8').split('\n')) {
    const line = rawLine.trim();
    if (line.startsWith('## ')) {
      if (curSection) sections.push({ name: curSection, nums: curProblems });
      curSection  = line.replace(/^##\s+\d+\.\s*/, '').trim();
      curProblems = [];
    } else if (/^\d+\.\s+/.test(line)) {
      const probName = line.replace(/^\d+\.\s+/, '').trim();
      const key = probName.toLowerCase().trim();
      let num = nameToNum[key];
      if (!num) {
        for (const [k, v] of Object.entries(nameToNum)) {
          if (k.includes(key) || key.includes(k)) { num = v; break; }
        }
      }
      if (num) curProblems.push(num);
    }
  }
  if (curSection) sections.push({ name: curSection, nums: curProblems });
  return sections;
}

