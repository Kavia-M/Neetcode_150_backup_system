function sectionPage(name, nums, numMap) {
  const items = nums
    .filter(n => numMap[n])
    .map(n => `<li>${numMap[n].name}</li>`)
    .join('\n');
  return `
  <div class="page section-page">
    <h1>${name}</h1>
    <ol>${items}</ol>
  </div>`;
}

