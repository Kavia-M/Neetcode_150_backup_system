// ── Main ─────────────────────────────────────────────────────────────────────
(async () => {
  const meta   = JSON.parse(fs.readFileSync(META_FILE, 'utf8'));
  const numMap = {};
  for (const p of meta.problems) numMap[p.num] = p;
  const sections = parseSections(meta);

  const parts = [];
  parts.push(coverPage());
  parts.push(creditsPage());

  for (let secIdx = 0; secIdx < sections.length; secIdx++) {
    const { name, nums } = sections[secIdx];
    parts.push(sectionPage(name, nums, numMap));

    for (let qIdx = 0; qIdx < nums.length; qIdx++) {
      const n = nums[qIdx];
      if (!numMap[n]) continue;
      const folder = path.join(BACKUP, numMap[n].folder);

      // Problem Statement
      const psFile = path.join(folder, 'Problem_Statement.md');
      if (fs.existsSync(psFile)) {
        let md = fs.readFileSync(psFile, 'utf8');
        md = await embedImages(md);
        const html = mdToHtml(md);
        parts.push(`<div class="problem-block">${html}</div>`);
      }

      // Hint Page (optional)
      const hintFile = path.join(folder, 'Hint_page.md');
      if (fs.existsSync(hintFile)) {
        let md = fs.readFileSync(hintFile, 'utf8');
        md = await embedImages(md);
        const html = mdToHtml(md);
        parts.push(`<div class="hint-block">${html}</div>`);
      }

      // Solution
      let solFile = path.join(folder, 'Solution.md');
      if (!fs.existsSync(solFile)) solFile = path.join(folder, 'Solution.txt');
      if (fs.existsSync(solFile)) {
        let md = fs.readFileSync(solFile, 'utf8');
        md = await embedImages(md);
        const html = mdToHtml(md);
        parts.push(`<div class="solution-block">${html}</div>`);
      }
    }
  }

  const fullHtml = `<!DOCTYPE html>
<html>
<head>
<meta charset="utf-8">
<style>${CSS}</style>
</head>
<body>
${parts.join('\n')}
</body>
</html>`;

  console.log('Launching browser...');
  const browser = await puppeteer.launch({ headless: true });
  const page    = await browser.newPage();

  await page.setContent(fullHtml, { waitUntil: 'networkidle0' });

  console.log('Rendering PDF...');
  await page.pdf({
    path: OUT_PDF,
    format: 'A4',
    printBackground: true,
    margin: { top: '1.6cm', bottom: '1.6cm', left: '2cm', right: '2cm' },
    displayHeaderFooter: true,
    headerTemplate: '<span></span>',
    footerTemplate: `<div style="width:100%;text-align:center;font-size:9px;font-family:Palatino,'Palatino Linotype',Georgia,serif;color:#333;padding:0 2cm;">
      <span class="pageNumber"></span>
    </div>`,
  });

  await browser.close();
  console.log('✅ PDF saved:', OUT_PDF);
})();
